package org.sunspotworld.math;

public class Quaternion {
    public static Quaternion IDENTITY = new Quaternion(0, 0, 0, 0);

    private float x;
    private float y;
    private float z;
    private float w;

    public Quaternion(float x, float y, float z, float w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }

    public Quaternion(Quaternion q) {
        this.x = q.x;
        this.y = q.y;
        this.z = q.z;
        this.w = q.z;
    }
}
